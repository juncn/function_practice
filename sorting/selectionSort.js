// O(n^2) time
// O(1) space

const selectionSort = array => {
  for (let i = 0; i < array.length; i++) {
    let idxOfMinValue = i;

    // Find the index of min value
    for (let j = i + 1; j < array.length; j++) {
      if (array[j] < array[idxOfMinValue]) {
        idxOfMinValue = j;
      }
    }

    // swap element
    const temp = array[i];
    array[i] = array[idxOfMinValue];
    array[idxOfMinValue] = temp;
  }

  return array;
}

